function getCharRadical(char: string): string | null {
  for (const [radical, chars] of Object.entries(RADICAL_MAP)) {
    if (chars.includes(char)) {
      return radical
    }
  }
  return null
}

function scoreSimilarity(char1: string, char2: string): number {
  let score = 0

  const radical1 = getCharRadical(char1)
  const radical2 = getCharRadical(char2)
  if (radical1 && radical2 && radical1 === radical2) {
    score += 50
  }

  if (char1 === char2) {
    score -= 1000
  }

  if (lookupCharacter(char2)) {
    score += 20
  }

  const commonIndex = COMMON_CHARS.indexOf(char2)
  if (commonIndex !== -1) {
    score += Math.max(0, 30 - Math.floor(commonIndex / 5))
  }

  return score
}

export function recommendSimilarCharacters(missingChar: string, count: number = 3): string[] {
  const allChars = getAllCharacters()
  const scored: { char: string; score: number }[] = []

  const similarFromMap = SIMILAR_CHARS_LOOKUP[missingChar] || []
  for (const char of similarFromMap) {
    if (lookupCharacter(char) && char !== missingChar) {
      scored.push({ char, score: 100 })
    }
  }

  for (const char of allChars) {
    if (char === missingChar) continue
    const score = scoreSimilarity(missingChar, char)
    if (score > 0) {
      const existing = scored.find(s => s.char === char)
      if (!existing) {
        scored.push({ char, score })
      }
    }
  }

  for (const char of COMMON_CHARS) {
    if (char === missingChar) continue
    if (lookupCharacter(char) && !scored.find(s => s.char === char)) {
      scored.push({ char, score: 10 })
    }
  }

  scored.sort((a, b) => b.score - a.score)

  return scored.slice(0, count).map(s => s.char)
}
